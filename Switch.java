public class Switch{

public static void main(String args[]){

String name="sakthi";

switch(name){

case "kavin":
System.out.println("KAVIN BLOCK");
break;

case "sakthi":
System.out.println("SAKTHI BLOCK");
break;

case "kumar":
 System.out.println("KUMAR BLOCK");
break;

case "vel":
 System.out.println("VEL BLOCK");
break;



default:
 System.out.println("DEFAULT BLOCK");

}

}


}